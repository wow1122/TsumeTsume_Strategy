/// <summary>
/// 敵AIの性格（Phase 17）。StageData の配置ごとに指定する（未指定＝突撃型）。
///
///   突撃型（Assault）  … 従来どおり。攻撃できる相手を探し、いなければ目標へ近づく。
///   待ち伏せ型（Ambush）… 挑発されるまで一切動かない。
///       挑発の条件は「被弾している（HPが減っている）」または
///       「今すぐ攻撃できる相手がいる（攻撃すること自体が挑発）」の2つ。
///       一度挑発されたら永続で、以後はずっと突撃型と同じ動きになる。
///       動かないだけで、立ち位置による受動的な効果（隣の味方への挟撃ガード）は効く。
///
/// 実際の判定は EnemyAI（挑発と行動の決定）と Unit.IsProvoked（挑発済みフラグ）が担う。
/// </summary>
public enum EnemyAIProfile
{
    Assault = 0, // 突撃型（既定）
    Ambush = 1,  // 待ち伏せ型
}
