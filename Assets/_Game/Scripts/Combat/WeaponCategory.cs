/// <summary>
/// 武器の大分類。戦闘ルール（三すくみ・挟撃・射程の伸び）はこの分類で決まる。
///   前衛（Melee） … 剣・斧・槍。三すくみ有り・挟撃に参加できる・隣のマスを攻撃
///   後衛（Ranged）… 弓・魔法。三すくみ無し・挟撃に参加できない・離れたマスを攻撃
/// ※ Melee = 0 は変更しないこと。category 欄を持たない古い .asset を読んだとき、
///   Unity は 0 として読み込むので「既存の剣・斧＝前衛」が自動的に正しくなる。
/// </summary>
public enum WeaponCategory
{
    Melee = 0,  // 前衛武器（剣・斧・槍）
    Ranged = 1, // 後衛武器（弓・魔法）
}
